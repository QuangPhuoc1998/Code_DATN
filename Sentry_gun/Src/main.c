void CDC_ReceiveCallBack(uint8_t *buf, uint32_t len)
{
	for(i = 0 ; i <64 ; i++)
	{
		buffer[i] = NULL;
	}
	for(i = 0 ; i <len ; i++)
	{
		buffer[i] = buf[i];
	}
	/*---*/
	if(buffer[0] == 'S')
	{
		for(dl_c=0;dl_c<5;dl_c++) buffer_count[dl_c] = 0;
		i = 1;
		dl_c=0;
		while (buffer[i] != ',')
    {
				buffer_count[dl_c] = buffer[i];
			  i++;
				dl_c++;
    }
		step_1_count =  atoi(buffer_count);
		for(dl_c=0;dl_c<5;dl_c++) buffer_count[dl_c] = 0;
		i++;
		dl_c=0;
		while (buffer[i] != ',')
    {
				buffer_count[dl_c] = buffer[i];
			  i++;
				dl_c++;
    }
		step_2_count =  atoi(buffer_count);
		if(step_1_count == 0) HAL_TIM_Base_Stop_IT(&htim2); else 
		{
			htim2.Instance -> ARR = step_1_count;
			HAL_TIM_Base_Start_IT(&htim2);
		}
		if(step_2_count == 0) HAL_TIM_Base_Stop_IT(&htim3); 
		else 
		{
			htim3.Instance -> ARR = step_2_count;
			HAL_TIM_Base_Start_IT(&htim3);
		}
	}
	else if(buffer[0] == 'Q')
	{
		if(buffer[1] == '0') HAL_TIM_Base_Stop_IT(&htim2);
		else if(buffer[1] == '1' && step_1_count != 0) HAL_TIM_Base_Start_IT(&htim2);
	}
	else if(buffer[0] == 'P')
	{
		if(buffer[1] == '0') HAL_TIM_Base_Stop_IT(&htim3);
		else if(buffer[1] == '1' && step_2_count != 0) HAL_TIM_Base_Start_IT(&htim3);
	}
	else if(buffer[0] == 'R')
	{
		if(buffer[1] == '0') HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_SET);
		else if(buffer[1] == '1') HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_RESET);
	}
	else if(buffer[0] == 'G')
	{
		if(buffer[1] == '0') HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4, GPIO_PIN_SET);
		else if(buffer[1] == '1') HAL_GPIO_WritePin(GPIOB, GPIO_PIN_4, GPIO_PIN_RESET);
	}
	else if(buffer[0] == 'B')
	{
		if(buffer[1] == '0') HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_SET);
		else if(buffer[1] == '1') HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_RESET);
	}
	else if(buffer[0] == 'Y')
	{
		if(buffer[1] == '0') HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_SET);
		else if(buffer[1] == '1') HAL_GPIO_WritePin(GPIOA, GPIO_PIN_15, GPIO_PIN_RESET);
	}
	/*---*/
}
